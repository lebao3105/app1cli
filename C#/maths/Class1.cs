namespace maths;
public class maths
{
    public static void Add(int num1, int num2) {
        Console.WriteLine(num1 + num2);
    }
    public static void Sub(int num1, int num2) {
        Console.WriteLine(num1 - num2);
    }
    public static void Multi(int num1, int num2) {
        Console.WriteLine(num1 * num2);
    }
    public static void Div(int num1, int num2) {
        Console.WriteLine(num1 / num2);
    }
}
